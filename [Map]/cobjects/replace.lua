addEventHandler("onClientResourceStart", resourceRoot,
	function()
	setElementFrozen(localPlayer, true)
	
		setTimer(
			function()
				for k, v in pairs(objects.replace) do
					local model = "files/" .. v[2] .. ".dff"
					local texture = "files/" .. v[3] .. ".txd"
					local collision = "files/cols/" .. v[2] .. ".col"
				    setElementDoubleSided(obj, true)
					if not fileExists(model) then
						outputDebugString("This: " .. model .. " doesn't exist.")
					elseif not fileExists(texture) then
						outputDebugString("This: " .. texture .. " doesn't exist.")
					elseif not fileExists(collision) then
						outputDebugString("This: " .. collision .. " doesn't exist.")
					else
						engineReplaceCOL(engineLoadCOL(collision), v[1])
						engineImportTXD(engineLoadTXD(texture), v[1])
						engineReplaceModel(engineLoadDFF(model, v[1]), v[1])
					end
				end
			end, 500, 1)
		setTimer(
			function()
				setCameraTarget(localPlayer)
				setElementFrozen(localPlayer, false)
			end, 1000, 1)
	end
)


    addEventHandler("onClientResourceStop", resourceRoot,
            function()
                    for k, v in pairs(objects.recreate) do
                            restoreWorldModel(v[1], 1, v[2], v[3], v[4], 0)
                            if v[8] and v[8] > 0 then restoreWorldModel(v[8], 1, v[2], v[3], v[4], 0) end
                    end
     
                    for k, v in pairs(objects.remove) do
                            restoreWorldModel(v[1], v[2], v[3], v[4], v[5])
                    end
            end
    )
